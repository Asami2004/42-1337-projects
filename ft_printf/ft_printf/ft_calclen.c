#include "ft_printf.h"

int	calclen(int n)
{
	int	len;

	len = 0;
	if (n <= 0)
	{
		len++;
		n = -n;
		while (n != 0)
		{
			n = n / 10;
			len++;
		}
	}
	else if (n > 0)
	{
		while (n != 0)
		{
			n = n / 10;
			len++;
		}
	}
	return (len);
}
